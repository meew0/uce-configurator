function convertToShiftJIS(str) {
    const codes = [];
    for (const char of str.split('')) {
        if (SINGLE_BYTE.hasOwnProperty(char)) {
            codes.push(SINGLE_BYTE[char]);
        } else if (DOUBLE_BYTE.hasOwnProperty(char)) {
            const code = DOUBLE_BYTE[char];
            codes.push(code >> 8, code & 0xff);
        } else {
            console.warn(`Could not encode character ${char} to Shift-JIS!`);
            codes.push(0x81, 0xa6); // ※
        }
    }

    codes.push(0);
    return codes;
}

function writeDialogue(snr, snrPos, element, layouter) {
    snr.setUint8(snrPos, 0x86);
    snrPos = writeBytes(snr, snrPos + 1, element.header);
    return writeStr(snr, snrPos, layouter.layout(element.text));
}
